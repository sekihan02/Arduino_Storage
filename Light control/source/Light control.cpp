#include "mariamole_auto_generated.h"

//---------------------------------------------------------------------------

void setup() {                
	pinMode(9, OUTPUT);     
}

//---------------------------------------------------------------------------

void loop() {
	int i;
	for(i = 0; i <= 255; i++)
	{
		analogWrite(9, i);
		delay(10);
	}
	for(i = 255; i >= 0; i--)
	{
		analogWrite(9, i);
		delay(10);
	}    	
}

